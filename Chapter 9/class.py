#class creation
class vehical:
    #class attributes
    color = "red"
    petrolOrDiesel = "petrol"
    mileage = "15km/l"

# Create an object of the class
car = vehical()
print(car.color)
bike = vehical()
print(bike.mileage)
print(bike.petrolOrDiesel)
print(bike.color)


aeroplane = vehical()
print(aeroplane.mileage)
print(aeroplane.petrolOrDiesel)
print(aeroplane.color)

