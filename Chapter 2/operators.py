#arithmetic operators in python
x = 10
y= 3
sum = x+y
diff = x - y
prod = x * y
quotient = x / y
floor_div = x // y
modulus = x % y
exponent = x ** y
print("Sum:", sum)
print("Difference:", diff)
print("Product:", prod)
print("Quotient:", quotient)
print("Floor Division:", floor_div)
print("Modulus:", modulus)
print("Exponentiation:", exponent)
